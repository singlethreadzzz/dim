package com.singlethreadzzz.dim.pojo;

public class SalesVolumeTypeInfo {
	
	private String goodsTypeCode;
	
	private String goodsTypeName;
	
	private float salesTypeVolume;

	public String getGoodsTypeCode() {
		return goodsTypeCode;
	}

	public void setGoodsTypeCode(String goodsTypeCode) {
		this.goodsTypeCode = goodsTypeCode;
	}

	public String getGoodsTypeName() {
		return goodsTypeName;
	}

	public void setGoodsTypeName(String goodsTypeName) {
		this.goodsTypeName = goodsTypeName;
	}

	public float getSalesTypeVolume() {
		return salesTypeVolume;
	}

	public void setSalesTypeVolume(float salesTypeVolume) {
		this.salesTypeVolume = salesTypeVolume;
	}

}
